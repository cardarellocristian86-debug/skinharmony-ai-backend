function tenantContextHeader(tenantId, signingSecret) {
  if (!signingSecret || signingSecret.length < 32) return "";
  const context = { version: "mcp_tenant_context_v1", tenant_id: tenantId, issued_at: new Date().toISOString() };
  const canonical = JSON.stringify(context);
  const assertion = `mtc_${crypto.createHmac("sha256", signingSecret).update(`mcp-tenant-context\u0000${canonical}`).digest("hex")}`;
  return Buffer.from(JSON.stringify({ ...context, assertion })).toString("base64url");
}

function ownerContextCanonical(context) {
  return JSON.stringify({
    version: context.assertion_version,
    audience: context.audience,
    tenant_id: context.tenant_id,
    access_mode: context.access_mode,
    role: context.role,
    delegated_actor: context.delegated_actor,
    owner_verified: context.owner_verified,
    owner_subject_fingerprint: context.owner_subject_fingerprint,
    issued_at: context.issued_at,
    binding_version: context.binding_version,
    binding_hash: context.binding_hash,
    approval_digest: context.approval_digest,
  });
}

function stableCanonical(value) {
  if (Array.isArray(value)) return value.map(stableCanonical);
  if (!value || typeof value !== "object") return value;
  return Object.keys(value).sort().reduce((result, key) => {
    if (value[key] !== undefined) result[key] = stableCanonical(value[key]);
    return result;
  }, {});
}

function nyraDeepBranchV2RequestId() {
  // Request correlation is generated by the MCP service, never accepted from
  // a tool argument. This prevents a caller from choosing an attestation
  // binding that could collide with another request.
  return `mcpv2_${crypto.randomBytes(16).toString("hex")}`;
}

function boundedIdentifier(value, field) {
  const normalized = String(value || "").trim();
  if (!NYRA_DEEP_BRANCH_V2_IDENTIFIER.test(normalized)) throw new Error(`nyra_deep_branch_v2_${field}_invalid`);
  return normalized;
}

function boundedIdentifierList(values, field, maximum = 50) {
  if (!Array.isArray(values) || values.length > maximum) throw new Error(`nyra_deep_branch_v2_${field}_invalid`);
  const normalized = values.map((value) => boundedIdentifier(value, field));
  if (new Set(normalized).size !== normalized.length) throw new Error(`nyra_deep_branch_v2_${field}_duplicate`);
  return normalized;
}

function boundedOptionalText(value, field, maximum) {
  if (value === undefined || value === null || value === "") return undefined;
  const normalized = String(value).trim();
  if (!normalized || normalized.length > maximum) throw new Error(`nyra_deep_branch_v2_${field}_invalid`);
  return normalized;
}

function boundedProbability(value, field) {
  if (value === undefined || value === null || value === "") return undefined;
  const numeric = Number(value);
  if (!Number.isFinite(numeric) || numeric < 0 || numeric > 1) throw new Error(`nyra_deep_branch_v2_${field}_invalid`);
  return numeric;
}

function normalizeEvidencePack(input) {
  const evidencePack = input && typeof input === "object" && !Array.isArray(input) ? input : null;
  const sources = Array.isArray(evidencePack?.sources) ? evidencePack.sources : null;
  const claims = Array.isArray(evidencePack?.claims) ? evidencePack.claims : null;
  if (!sources || !claims || sources.length < 1 || claims.length < 1 || sources.length > 20 || claims.length > 30) {
    throw new Error("nyra_deep_branch_v2_evidence_pack_invalid");
  }
  const sourceTypes = new Set(["official", "regulator", "academic", "standards", "manufacturer", "news", "industry", "community", "other"]);
  const claimKinds = new Set(["fact", "inference", "hypothesis"]);
  const normalizedSources = sources.map((source) => {
    if (!source || typeof source !== "object" || Array.isArray(source)) throw new Error("nyra_deep_branch_v2_evidence_source_invalid");
    const sourceType = boundedOptionalText(source.source_type, "evidence_source_type", 40);
    if (!sourceType || !sourceTypes.has(sourceType)) throw new Error("nyra_deep_branch_v2_evidence_source_type_invalid");
    const url = boundedOptionalText(source.url, "evidence_source_url", 2_048);
    if (!url) throw new Error("nyra_deep_branch_v2_evidence_source_url_invalid");
    try {
      const parsedUrl = new URL(url);
      if (parsedUrl.protocol !== "https:" || parsedUrl.username || parsedUrl.password) {
        throw new Error("unsafe_evidence_source_url");
      }
    } catch {
      throw new Error("nyra_deep_branch_v2_evidence_source_url_invalid");
    }
    const title = boundedOptionalText(source.title, "evidence_source_title", 500);
    if (!title) throw new Error("nyra_deep_branch_v2_evidence_source_title_invalid");
    const excerpt = boundedOptionalText(source.excerpt, "evidence_source_excerpt", 1_200);
    if (!excerpt || excerpt.length < 16) throw new Error("nyra_deep_branch_v2_evidence_source_excerpt_required");
    return {
      id: boundedIdentifier(source.id, "evidence_source_id"),
      url,
      title,
      ...(boundedOptionalText(source.publisher, "evidence_source_publisher", 240) ? { publisher: boundedOptionalText(source.publisher, "evidence_source_publisher", 240) } : {}),
      source_type: sourceType,
      ...(boundedOptionalText(source.published_at, "evidence_source_published_at", 80) ? { published_at: boundedOptionalText(source.published_at, "evidence_source_published_at", 80) } : {}),
      ...(boundedOptionalText(source.fetched_at, "evidence_source_fetched_at", 80) ? { fetched_at: boundedOptionalText(source.fetched_at, "evidence_source_fetched_at", 80) } : {}),
      excerpt,
      ...(boundedOptionalText(source.summary, "evidence_source_summary", 1_200) ? { summary: boundedOptionalText(source.summary, "evidence_source_summary", 1_200) } : {}),
    };
  });
  if (new Set(normalizedSources.map((source) => source.id)).size !== normalizedSources.length) {
    throw new Error("nyra_deep_branch_v2_evidence_source_id_duplicate");
  }
  const sourceIds = new Set(normalizedSources.map((source) => source.id));
  const normalizedClaims = claims.map((claim) => {
    if (!claim || typeof claim !== "object" || Array.isArray(claim)) throw new Error("nyra_deep_branch_v2_evidence_claim_invalid");
    const kind = boundedOptionalText(claim.kind, "evidence_claim_kind", 40);
    if (!kind || !claimKinds.has(kind)) throw new Error("nyra_deep_branch_v2_evidence_claim_kind_invalid");
    const sourceReferences = boundedIdentifierList(claim.source_ids, "evidence_claim_source_ids", 20);
    if (sourceReferences.length < 1 || sourceReferences.some((sourceId) => !sourceIds.has(sourceId))) {
      throw new Error("nyra_deep_branch_v2_evidence_claim_source_invalid");
    }
    const normalized = {
      id: boundedIdentifier(claim.id, "evidence_claim_id"),
      kind,
      text: boundedOptionalText(claim.text, "evidence_claim_text", 2_000),
      source_ids: sourceReferences,
    };
    if (!normalized.text) throw new Error("nyra_deep_branch_v2_evidence_claim_text_invalid");
    const contradictory = claim.contradicts_claim_ids === undefined
      ? undefined
      : boundedIdentifierList(claim.contradicts_claim_ids, "evidence_claim_contradicts", 20);
    if (contradictory) normalized.contradicts_claim_ids = contradictory;
    const confidence = boundedProbability(claim.confidence, "evidence_claim_confidence");
    if (confidence !== undefined) normalized.confidence = confidence;
    return normalized;
  });
  if (new Set(normalizedClaims.map((claim) => claim.id)).size !== normalizedClaims.length) {
    throw new Error("nyra_deep_branch_v2_evidence_claim_id_duplicate");
  }
  return { sources: normalizedSources, claims: normalizedClaims };
}

function normalizeRequirementBindings(values) {
  if (values === undefined) return [];
  if (!Array.isArray(values) || values.length > 64) throw new Error("nyra_deep_branch_v2_requirement_bindings_invalid");
  const normalized = values.map((binding) => {
    if (!binding || typeof binding !== "object" || Array.isArray(binding)) throw new Error("nyra_deep_branch_v2_requirement_binding_invalid");
    const requirementRef = String(binding.requirement_ref || "").trim();
    if (!NYRA_DEEP_BRANCH_V2_REQUIREMENT_REF.test(requirementRef)) throw new Error("nyra_deep_branch_v2_requirement_ref_invalid");
    const sourceIds = binding.source_ids === undefined ? [] : boundedIdentifierList(binding.source_ids, "requirement_binding_source_ids", 20);
    const claimIds = binding.claim_ids === undefined ? [] : boundedIdentifierList(binding.claim_ids, "requirement_binding_claim_ids", 30);
    if (sourceIds.length < 1 || claimIds.length < 1) throw new Error("nyra_deep_branch_v2_requirement_binding_evidence_required");
    return {
      id: boundedIdentifier(binding.id, "requirement_binding_id"),
      requirement_ref: requirementRef,
      source_ids: sourceIds,
      claim_ids: claimIds,
    };
  });
  if (new Set(normalized.map((binding) => binding.id)).size !== normalized.length) {
    throw new Error("nyra_deep_branch_v2_requirement_binding_id_duplicate");
  }
  return normalized;
}

function nyraDeepBranchV2EvidencePackHash(evidencePack, requirementBindings) {
  return crypto
    .createHash("sha256")
    .update(JSON.stringify(stableCanonical({ evidence_pack: evidencePack, requirement_bindings: requirementBindings })))
    .digest("hex");
}

