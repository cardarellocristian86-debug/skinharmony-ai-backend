function createNyraDeepBranchV2RequestAttestation(config, {
  tenantId,
  requestId,
  operation,
  branchId,
  subbranchId,
  evidenceRefs,
  evidencePackHash,
}) {
  const issuedAt = new Date().toISOString();
  const nonce = crypto.randomBytes(16).toString("hex");
  const payload = nyraDeepBranchV2AttestationPayload({
    tenantId,
    requestId,
    operation,
    branchId,
    subbranchId,
    evidenceRefs,
    evidencePackHash,
    issuedAt,
    nonce,
  });
  const signature = crypto
    .createHmac("sha256", config.nyraDeepBranchV2RequestSigningSecret)
    .update(`nyra-deep-branch-v2-request\u0000${JSON.stringify(stableCanonical(payload))}`)
    .digest("hex");
  return {
    schema_version: NYRA_DEEP_BRANCH_V2_REQUEST_ATTESTATION_VERSION,
    issuer: NYRA_DEEP_BRANCH_V2_REQUEST_ATTESTATION_ISSUER,
    ...payload,
    max_age_seconds: NYRA_DEEP_BRANCH_V2_REQUEST_MAX_AGE_SECONDS,
    signature,
  };
}

