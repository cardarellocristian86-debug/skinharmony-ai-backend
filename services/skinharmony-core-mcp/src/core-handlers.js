function safeNyraDeepBranchV2Code(value, fallback = "unknown") {
  const normalized = String(value || "").trim();
  return /^[a-zA-Z0-9][a-zA-Z0-9_.:-]{0,119}$/.test(normalized) ? normalized : fallback;
}

function compactNyraDeepBranchV2Reasons(values) {
  const list = Array.isArray(values) ? values : values === undefined || values === null ? [] : [values];
  const reasons = [];
  for (const value of list) {
    const candidate = typeof value === "string" ? value : value?.code;
    const reason = safeNyraDeepBranchV2Code(candidate, "");
    if (reason && !reasons.includes(reason) && reasons.length < 12) reasons.push(reason);
  }
  return reasons;
}

function compactNyraDeepBranchV2Lineage(runtime) {
  const lineage = runtime?.evaluation?.lineage || runtime?.lineage || [];
  const candidates = Array.isArray(lineage)
    ? lineage
    : Array.isArray(lineage?.nodes)
      ? lineage.nodes
      : [];
  if (!Array.isArray(candidates)) return [];
  // The full tree is intentionally not a ChatGPT tool payload. Six concrete
  // L2–L4 lineage nodes are enough to show the governing route while keeping
  // contracts, inputs and evidence opaque.
  return candidates.slice(0, 6).map((node) => {
    const confidence = Number(node?.confidence);
    return {
      state: safeNyraDeepBranchV2Code(node?.state, "unknown"),
      id: safeNyraDeepBranchV2Code(node?.node_id || node?.id, "redacted"),
      level: Number.isInteger(Number(node?.level)) && Number(node.level) >= 2 && Number(node.level) <= 4
        ? Number(node.level)
        : null,
      node_type: safeNyraDeepBranchV2Code(node?.node_type, "unknown"),
      confidence: Number.isFinite(confidence) && confidence >= 0 && confidence <= 100 ? confidence : null,
      reasons: compactNyraDeepBranchV2Reasons(node?.reasons || node?.reason_codes),
    };
  });
}

