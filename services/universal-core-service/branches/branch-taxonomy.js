function byParent(parentId) {
  return TAXONOMY_NODES.filter((node) => node.parent_id === parentId);
}

function flattenDescendants(nodeId, accumulator = new Set()) {
  for (const node of byParent(nodeId)) {
    accumulator.add(node.node_id);
    flattenDescendants(node.node_id, accumulator);
  }
  return [...accumulator];
}

export function deterministicBranchTaxonomy() {
  return {
    schema_version: "branch_taxonomy_v1",
    max_supported_depth: 20,
    nodes: TAXONOMY_NODES.map((node) => ({
      ...node,
      child_count: byParent(node.node_id).length,
      descendant_node_ids: flattenDescendants(node.node_id),
    })),
    synaptic_links: SYNAPTIC_LINKS,
  };
}
