export function createI18n(state) {
  function currentLanguage() {
  const language = state.settings?.appLanguage || "it";
  return supportedLanguages.includes(language) ? language : "it";
}

  function currentLocale() {
  return languageLocales[currentLanguage()] || "it-IT";
}

  function t(key, variables = {}) {
  const language = currentLanguage();
  const tree = translations[language] || translations.it;
  const fallback = translations.it;
  const parts = key.split(".");
  const read = (source) => parts.reduce((value, part) => (value && typeof value === "object" ? value[part] : undefined), source);
  let message = read(tree) ?? read(fallback) ?? key;
  Object.entries(variables).forEach(([name, value]) => {
    message = String(message).replaceAll(`{{${name}}}`, String(value));
  });
  return String(message);
}

  return { currentLanguage, currentLocale, t };
}
